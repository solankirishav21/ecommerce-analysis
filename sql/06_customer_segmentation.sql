WITH rfm_base AS (
    SELECT
        o.customer_name,
        (SELECT MAX(order_date) + 1 FROM orders) - MAX(o.order_date) AS recency, 
        COUNT(DISTINCT o.order_id) AS frequency, 
        SUM(od.amount) AS monetary
    FROM
        orders AS o
    JOIN
        order_details AS od ON o.order_id = od.order_id
    GROUP BY
        o.customer_name
),rfm_scores AS (
    SELECT
        customer_name,
        recency,
        frequency,
        monetary,
        NTILE(4) OVER (ORDER BY recency DESC) AS r_score,  -- Higher score for lower (better) recency
        NTILE(4) OVER (ORDER BY frequency ASC)  AS f_score,  -- Higher score for higher (better) frequency
        NTILE(4) OVER (ORDER BY monetary ASC)  AS m_score   -- Higher score for higher (better) monetary
    FROM
        rfm_base
)
SELECT
    customer_name,
    recency,
    frequency,
    monetary,
    r_score,
    f_score,
    m_score,
    CASE
        WHEN r_score = 4 AND f_score = 4 AND m_score = 4 THEN 'Champions'
        WHEN r_score = 4 AND f_score >= 3 THEN 'Loyal Customers'
        WHEN r_score >= 3 AND f_score >= 3 AND m_score >= 3 THEN 'Potential Loyalists'
        WHEN r_score = 4 THEN 'Recent Customers'
        WHEN r_score <= 2 AND f_score <= 2 THEN 'At Risk'
        WHEN r_score = 1 AND f_score = 1 THEN 'Hibernating'
        ELSE 'Others'
    END AS customer_segment
FROM
    rfm_scores
ORDER BY
    r_score DESC, f_score DESC, m_score DESC;
